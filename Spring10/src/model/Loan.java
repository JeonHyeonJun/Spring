package model;

public class Loan {
	public int idx;
	public int readyMonth;
	public int thisMonthOrigin;
	public int thisMonthRate;
	public int amount;
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getReadyMonth() {
		return readyMonth;
	}
	public void setReadyMonth(int readyMonth) {
		this.readyMonth = readyMonth;
	}
	public int getThisMonthOrigin() {
		return thisMonthOrigin;
	}
	public void setThisMonthOrigin(int thisMonthOrigin) {
		this.thisMonthOrigin = thisMonthOrigin;
	}
	public int getThisMonthRate() {
		return thisMonthRate;
	}
	public void setThisMonthRate(int thisMonthRate) {
		this.thisMonthRate = thisMonthRate;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
}
